            // region: Group 10
            this.moveItem('contact', 'group10', 'contact', 1);
            this.moveItem('contact2', 'group10', 'contact2', 1);
            // endregion
        },
        moveItem(elementName, group, name, speed) {
            const element = document.getElementById(elementName);
            if (this.existStyle(name, group)) {
                this[group][name] = element.style.left;
            }
            element.style.left = `calc(${this[group][name]} - ${this.scrolled * speed}px)`;
        },
        existStyle(name, group) {
            if (this[group][name] === '') {
                return true;
            }
            return false;
        },
        applyScaleFactor(elementName) {
            const element = document.getElementById(elementName);
            element.style.bottom *= this.scaleFactor;
        },
    },
    beforeMount() {
        window.addEventListener('scroll', this.handleScroll);
        this.width = window.innerWidth;
    },
    beforeDestroy() {
        window.removeEventListener('scroll', this.handleScroll);
    },
};
</script>

